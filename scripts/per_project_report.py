"""Emit a per-project Markdown report.

For each chess-* project, produce:
  reports/projects/<name>.md        human-readable evidence dossier
  data/projects/<name>.json         machine-readable appendix

The report contains:
  - Factual metadata (language, agent, period, LOC, git)
  - Chess feature coverage (what was implemented)
  - Session summary (prompts, tokens, cost, tools used)
  - User-driven backlog (BL-###) extracted from user prompts
  - Canonical "PL-ROOT" (first non-trivial human prompt)
  - Interaction analysis (categories of prompts, avg length)
  - Evidence pointers: [R:], [T:], [G:], [S:]

Usage:
  python3 scripts/per_project_report.py            # all projects
  python3 scripts/per_project_report.py chess-py   # one
"""

from __future__ import annotations

import json
